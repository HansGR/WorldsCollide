"""Atlas room naming: area-code registry and room-id -> name mapping.

Hand-maintained; edit freely and run tools/compile_atlas.py to
re-validate.

Name formats:
  Rooms      CODE + world? + NN + subletter? + (-variant)?
             CODE   2-letter area code from AREA_CODES
             world  b = WoB, r = WoR, x = no world; only present for
                    areas that exist in both worlds (dropped where the
                    area lives entirely in one world)
             NN     ordinal within the area and world; WoB/WoR twins of
                    the same place share it (437 -> ALb01, 438 -> ALr01;
                    293 -> ZOb01, 293r -> ZOr01); logical and
                    mode-specific rooms occupy a band starting at 50
             sub    physical sub-room split (37a -> UNb05a)
             -variant  mode variant of the base room: -ruin, -dc,
                    -mapsafe, -branch, -share, -test (a variant keeps
                    its parent room's NN: ruin-zozo -> ZOr01-ruin,
                    dc-4 -> NAb01-dc)
  Roots      CODE + world? + -root(-suffix)?  (no ordinal: roots are
             connection points for the world model, not rooms)
  Map shuffle  MS + world + - + target area code (+digit when one area
             has several stubs): ms-wob-14 -> MSb-GF. Stubs connect the
             world map into their whole area.
  World map  the room id itself ('wob-narshe', 'wor-triangle', ...);
             0 -> 'wob', 1 -> 'wor'. These are already human-readable
             and belong to no other area.
  Kefka's Tower  rooms keep their structured ids (KTa1...): the lane
             letter (a/b/c) carries meaning.
  Cyan's Dream  is three lanes in game order, named like KT's:
             CDa (train), CDb (caves), CDc (Doma castle).

The compiler validates: bijection over data/rooms.room_data, unique
names, area code registered, world letter present exactly when the area
spans both worlds and consistent with the room's world field.
"""

